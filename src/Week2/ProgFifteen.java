package Week2;


/* 15. Write a Java program to swap two variables.*/
public class ProgFifteen {

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a;
          a = b;
          b = c;

        System.out.println("Swap Value a = " +a );
        System.out.println("Swap Value b = " +b);
    }


}
