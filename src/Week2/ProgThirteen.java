package Week2;
/*13. Write a Java program that takes three numbers as input to calculate and
print the average of the numbers.*/


public class ProgThirteen {
    public static void main(String[] args) {


        int a = 10; // int is data type and a is variable name
        int b = 15; // int is data type and a is variable name
        int c = 20; // int is data type and a is variable name
        int d = (a + b + c) / 3; // int is data type and a is variable name

        System.out.println("Average of three number = " + d);
    }
}